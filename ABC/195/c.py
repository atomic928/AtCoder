n = int(input())
ln = len(str(n))


if ln < 4:
  print(0)
  exit()
elif ln < 7:
  ans = n - 999
elif ln < 10:
  ans = (n - 999999)*2 + 10**6 - 10**3
elif ln < 13:
  ans = (n - 999999999)*3 + (10**9 - 10**6)*2 + 10**6 - 10**3
elif ln < 16:
  ans = (n - 999999999999)*4 + (10**12 - 10**9)*3 + (10**9 - 10**6)*2 + 10**6 - 10**3
else:
  ans = 5 + (10**15-10**12)*4 + (10**12 - 10**9)*3 + (10**9 - 10**6)*2 + 10**6 - 10**3

print(ans)